package server

import (
	"encoding/json"
	"io/ioutil"
)

type serverCfgFileInfo struct {
	PluginName string `json:"PluginName"`
	IpAddr     string `json:"BMCIpAddr"`
	Port       string `json:"Port"`
}

func parseCfgFile(cfgFileName string) (*serverCfgFileInfo, error) {
	var cfgInfo serverCfgFileInfo

	//Setup reasonable defaults when cfg parsing fails
	cfgFileData, err := ioutil.ReadFile(cfgFileName)
	if err == nil {
		_ = json.Unmarshal(cfgFileData, &cfgInfo)
	}
	return &cfgInfo, err
}
